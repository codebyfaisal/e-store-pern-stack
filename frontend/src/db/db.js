const randomDate = () => {
  const d = new Date();
  const currentYear = new Date().getFullYear();
  d.setDate(Math.floor(Math.random() * 30) + 1);
  d.setMonth(Math.floor(Math.random() * 12));
  d.setFullYear(Math.floor(Math.random() * (currentYear - 2024 + 1)) + 2024);
  d.setTime(d.getTime() + Math.floor(Math.random() * 24 * 60 * 60 * 1000));
  return d;
};

